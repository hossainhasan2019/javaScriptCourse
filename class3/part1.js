//  JavaScript Arithmetic Operators
//  Arithmetic operators are used to perform arithmetic on numbers:

//  Operator	Description
//  +	Addition
//  -	Subtraction
//  *	Multiplication
//  **	Exponentiation (ES2016)
//  /	Division
//  %	Modulus (Division Remainder)
//  ++	Increment
//  --	Decrement


//example...
let number1 = 10;
let number2 = 2;

console.log(number1 + number2); // 2ti valu jog kora
console.log(number1 - number2); // 2ti valu biog kora
console.log(number1 * number2); // 2ti valu gun kora
console.log(number1 / number2); // 2ti valu vag kora
console.log(number1 % number2); // 2ti valu vagses kora

console.log(number1 ** number2); // 2ti valu 2squre kora
