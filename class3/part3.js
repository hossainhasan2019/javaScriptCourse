/*
//  JavaScript Comparison Operators
//  Operator	Description
//  ==	equal to
//  ===	equal value and equal type
//  !=	not equal
//  !==	not equal value or not equal type
//  >	greater than
//  <	less than
//  >=	greater than or equal to
//  <=	less than or equal to
//  ?	ternary operator
let price = 100;
*/

let price = 200;
// greater than
console.log( price > 200);//price valu theke boro kina 
console.log( price >= 100); //greater than or equal to
console.log( price < 50); //  <	less than
console.log( price <= 500);//  <=	less than or equal to
console.log( price === 200);//  ===	equal value and equal type
console.log( price == 200);// //  ==	equal to
console.log( price !== 200); //  !==	not equal value or not equal type
