/*
//  JavaScript Logical Operators
//  Operator	Description

//  &&	    logical      and
//  ||  	logical      or
//  !	    logical      not
*/

//exampler

let price =10;
//logical oprator and usage
console.log(price > 5 && price < 15); // print true 
console.log(price > 50 && price < 15); // print false 

//logical oprator or usage
console.log(price > 50 || price < 15); // one expression true hole ei true return korbe

//logical oprator not usage
console.log(price > 5);// retuen true
console.log(!(price > 5));// retuen false usage not oprator

